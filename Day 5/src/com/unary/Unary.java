package com.unary;

public class Unary {
	public void postincrement(int a)
	{
		System.out.println(a++);
	}
	public void preincrement(int a)
	{
		System.out.println(++a);
	}
	public void postdecrement(int a)
	{
		System.out.println(a--);
	}
	public void predecrement(int a)
	{
		System.out.println(--a);
	}
}
